using namespace std;

class Solution {
public:
  bool search(vector<int>& nums, int target, int left, int right) {
    int size = nums.size();
    while (left < right) {
      if (left == right - 1) return nums[left] == target || nums[right] == target;

      int mid = (left + right) / 2;
      if (nums[mid] == target) return true;

      bool pivotLeft = true;
      if (nums[mid] < nums[left]) {
        pivotLeft = true;
      } else if (nums[mid] > nums[left]) {
        pivotLeft = false;
      } else {
        if (search(nums, target, left, mid)) return true;
        return search(nums, target, mid, right);
      }

      if (pivotLeft) { // 后半部分是正确有序的
        if (target < nums[mid]) {
          right = mid;
        } else if (target > nums[right]) {
          right = mid;
        } else {
          left = mid;
        }
      } else { // 前半部分是正确有序的
        if (target > nums[mid]) {
          left = mid;
        } else if (target < nums[left]) {
          left = mid;
        } else {
          right = mid;
        }
      }
    }
    return false;
  }

  bool search(vector<int>& nums, int target) {
    int size = nums.size();
    if (size == 1) {
      return target == nums[0];
    }
    return search(nums, target, 0, size - 1);
  }
};

